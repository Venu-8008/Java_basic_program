//Program for Assignment operator

public class AssignmentEx
{
	public static void main (String [] args)
	{
		int num1=15;
		System.out.println(num1);
		num1+=5;
		System.out.println(num1);
		num1-=3;
		System.out.println(num1);
		num1*=4;
		System.out.println(num1);
		num1/=2;
		System.out.println(num1);
		num1%=3;
		System.out.println(num1);
	}
}